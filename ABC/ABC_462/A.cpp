#include <bits/stdc++.h>

int main() {
    std::string S;
    std::cin >> S;

    for (char c : S) {
        if (c == '1') std::cout << c;
        else if (c == '2') std::cout << c;
        else if (c == '3') std::cout << c;
        else if (c == '4') std::cout << c;
        else if (c == '5') std::cout << c;
        else if (c == '6') std::cout << c;
        else if (c == '7') std::cout << c;
        else if (c == '8') std::cout << c;
        else if (c == '9') std::cout << c;
        else if (c == '0') std::cout << c;
        else continue;
    }
    std::cout << std::endl;
    return 0;
}

